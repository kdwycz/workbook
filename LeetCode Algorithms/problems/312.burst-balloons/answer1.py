# 分治 + 记忆化搜索

from typing import List
from functools import lru_cache


class Solution:
    def maxCoins(self, nums: List[int]) -> int:
        nums = [1] + nums + [1]

        @lru_cache(None)
        def _calu_max_coins(i, j):
            # 返回nums数组(i, j)中硬币的最大数量
            if j - i < 2:
                return 0
            res = 0
            for k in range(i+1, j):
                res = max(
                    res, 
                    _calu_max_coins(i, k) +
                    nums[i] * nums[k] * nums[j] +
                    _calu_max_coins(k, j)
                )
            return res
        
        return _calu_max_coins(0, len(nums)-1)
